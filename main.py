def _extract_clean_info(user_input: str, trigger_phrases: list) -> str:
    """মনে-রাখার কমান্ড থেকে ট্রিগার-ফ্রেজ ও নয়েজ শব্দ সরিয়ে আসল তথ্যটুকু বের করে।"""
    clean_info = user_input
    for phrase in sorted(trigger_phrases, key=len, reverse=True):
        clean_info = clean_info.replace(phrase, "")
        # কমন কেস-ভ্যারিয়েশনও চেষ্টা করা (যেহেতু user_input lower() করা হয়নি এখানে)
        clean_info = clean_info.replace(phrase.capitalize(), "")
    for noise_word in ["জারা", "রোজ", "যে", "এটা", "এইটা", "ওটা"]:
        clean_info = clean_info.replace(noise_word, "")
    # [বাগ ফিক্স] একাধিক স্পেস একত্র করা (ট্রিগার-ফ্রেজ সরানোর পর যে ফাঁকা জায়গা থেকে যায়)
    clean_info = re.sub(r"\s+", " ", clean_info)
    return clean_info.strip(" ,।:-")


# [Short-term Context] কোন ইনটেন্টের পর ফলো-আপ প্রশ্ন এলে সেটাকে কোন "টপিক" হিসেবে
